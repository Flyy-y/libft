#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*ptr;

	if (!(ptr = malloc(size)))
		return ((void*)0);
	while ((long)--size >= 0)
		((unsigned char*)ptr)[size] = (unsigned char)0;
	return (ptr);
}
